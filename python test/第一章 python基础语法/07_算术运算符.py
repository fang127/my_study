# 算数运算符
# + - * /
# //取整除 9//2 输出4
# %取余 9//2 输出1
# **指数 10**20 为10的20次方
print("1 + 1 =",1 + 1)
print("2 - 1 =",2 - 1)
print("2 * 3 =",2 * 3)
print("4 / 2 =",4 / 2)
print("7 // 2 =",7 // 2)
print("7 % 2 =",7 % 2)
print("7 ** 2 =",7 ** 2)

# 赋值运算符
# = += -= *= /= %= **= //=
# 赋值运算符
num = 1 + 2 + 3
# 复合赋值运算符
num = 1
num += 1 # num = num + 1
print("num += 1 :",num)
num -= 1
print("num -= 1 :",num)
num *= 4
print("num *= 4 :",num)
num /= 2
print("num /= 2 :",num)
num = 3
num %= 2
print("num %= 2 :",num)
num **= 2
print("num **= 2 :",num)
num //= 2
print("num //= 2 :",num)