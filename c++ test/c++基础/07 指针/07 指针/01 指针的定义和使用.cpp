#include <iostream>
using namespace std;

int main1()
{
	//1、定义指针
	int a = 10;

	//指针定义的语法：数据类型 * 指针变量
	int* p;//仅创建指针变量，并没有和a产生关联
	//让指针记录变量a的地址
	p = &a;
	cout << "a的地址为：" << &a << endl;
	cout << "指针p为：" << p << endl;

	//2、使用指针
	//可以通过解引用的方式来找到指针指向的内存
	cout << *p << endl;
	//指针前加*代表解引用，找到指针指向的内存中存放的数据,并进行修改和使用
	*p = 1000;
	cout << "a = " << a << endl;
	cout << "*p = " << *p << endl;


	system("pause");

	return 0;
}